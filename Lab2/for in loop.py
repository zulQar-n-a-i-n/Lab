# for in loop is used to iterate over a range 

for i in range(5):
    print(i)


# to iterate over a list

new_list = [12,3,76,5]
for value in new_list:
    print(value)


# to iterate over a tuple
mytup = (23,'hello',[1,2])
for value in mytup:
    print(value)

# to iterate over string
s = 'string'
for i in s:
    print(s)